#include "minishell.h"

/*
**	Renvoi 1 si le caractÃ¨re C un token, sinon elle renvoi 0.
*/
int	ft_is_token(char c)
{
	if (c == ' '
		|| c == '\n'
		|| c == '\\'
		|| c == '\''
		|| c == '"'
		|| c == '$'
		|| c == '|'
		|| c == '>'
		|| c == '<'
		|| c == ';')
		return (1);
	return (0);
}
